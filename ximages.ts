// Round up to the nearest multiple of 4 b. byte height must be a multiple of 4 b. There are 8 pixels per 4 b.
function imgByteHeight(height: number) {
    return 4*Math.ceil(height / 8);
}

function imgDecode(encoded: Buffer, makeImage: boolean = true) {
    let e = 0
    let width = encoded[e++] //crashes here
    let height = encoded[e++]
    let control = encoded[e++]
    let bit = 0

    let i = 0
    let hb = imgByteHeight(height)
    let buf = Buffer.create(8 + width * hb)
    // write header
    buf[i++] = 0x87
    buf[i++] = 4
    buf[i++] = width & 0xFF
    buf[i++] = width >> 8
    buf[i++] = height & 0xFF
    buf[i++] = height >> 8
    buf[i++] = 0
    buf[i++] = 0
    // decode image buffer
    while (e < encoded.length) {
        let val = encoded[e++]
        if (control & (1 << bit)) { // a run
            let runLen = encoded[e++]
            while (runLen--)
                buf[i++] = val
        }
        else
            buf[i++] = val          // a single pixel

        bit++
        if (bit == 8) {
            control = encoded[e++]
            bit = 0
        }
    }
    if (i != buf.length)
        console.log(`Bad decode length:${i} expected:${buf.length}`)

    if (makeImage) {
        return image.ofBuffer(buf)
    } else
        return null
}

